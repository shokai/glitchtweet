# -*- coding: utf-8 -*-
module Sekine
  def self.glitch(str)
    $KCODE = 'u'
    dic = {
      'さ' => 'しゃ',
      'す' => 'しゅ',
      'せ' => 'しぇ',
      'そ' => 'しょ',
      'ざ' => 'じゃ',
      'ず' => 'じゅ',
      'ぜ' => 'じぇ',
      'ぞ' => 'じょ',
      'サ' => 'シャ',
      'ス' => 'シュ',
      'セ' => 'シェ',
      'ソ' => 'ショ',
      'ザ' => 'ジャ',
      'ズ' => 'ジュ',
      'ゼ' => 'ジェ',
      'ゾ' => 'ジョ'
    }
    dic.each{|k,v|
      str.gsub!(k,v)
    }
    return str
  end
end
